"""
Tipo de tipagem = Dinâmica /  Forte
Strings são textos que estão dentro de aspas

Podemos utilizar aspas simples ou aspas duplas
"""
#Aspas simples
print('Texto')

#Aspas duplas
print("Texto")

#Metodos para aparecer aspas
print('Programação "Básica"')

#Escape
#Utilizado para quando precisamos utilias as aspas dentro do texto
print("Programação \"Básica\"")

#r
print(r"Programação \"Básica\"")